"""
Normalize validator-owned signal bindings for the unified signal model.

This migration repairs two rollout issues from the initial SignalDefinition
backfill:

1. Library-validator input bindings were created with
   ``source_scope='submission_payload'`` even for signals declared as
   submission metadata.
2. ``SignalDefinition.provider_binding`` stored the old mixed
   ``binding_config`` payload, including submission-source selectors that
   should now live only on ``StepSignalBinding``.

The migration:

- creates any missing validator-owned input bindings for existing steps
- repairs bindings that still match the old auto-generated defaults
- normalizes provider_binding on validator-owned signals
"""

from django.db import migrations

LEGACY_SOURCE_SCOPE_MAP = {
    "submission": "submission_payload",
    "payload": "submission_payload",
    "submission.payload": "submission_payload",
    "submission_payload": "submission_payload",
    "submission.metadata": "submission_metadata",
    "submission_metadata": "submission_metadata",
    "system": "system",
    "upstream_step": "upstream_step",
}

_SUBMISSION_SOURCE_KEYS = (
    "default_value",
    "is_required",
    "path",
    "source",
    "source_data_path",
    "source_scope",
)


def _build_step_binding_defaults(mapping, *, fallback_path, default_required):
    """Return canonical StepSignalBinding defaults from a legacy mapping."""
    binding = dict(mapping or {})
    source = binding.get("source")

    source_scope = (
        binding.get("source_scope")
        or LEGACY_SOURCE_SCOPE_MAP.get(source)
        or "submission_payload"
    )

    source_data_path = binding.get("source_data_path")
    if source_data_path in (None, "") and source in LEGACY_SOURCE_SCOPE_MAP:
        source_data_path = binding.get("path")
    if source_data_path in (None, ""):
        source_data_path = fallback_path

    default_value = binding.get("default_value")
    is_required = binding.get("is_required", default_required)

    return {
        "default_value": default_value,
        "is_required": is_required,
        "source_data_path": source_data_path or "",
        "source_scope": source_scope,
    }


def _normalize_provider_binding(mapping):
    """Strip submission-source selectors from provider_binding JSON."""
    binding = dict(mapping or {})
    source = binding.get("source")

    if source == "metric":
        provider_binding = dict(binding)
        metric_key = (
            provider_binding.pop("metric_key", None)
            or provider_binding.pop("key", None)
        )
        provider_binding.pop("path", None)
        provider_binding.pop("source", None)
        if metric_key:
            provider_binding["metric_key"] = metric_key
        return provider_binding

    if (
        source in LEGACY_SOURCE_SCOPE_MAP
        or "source_scope" in binding
        or "source_data_path" in binding
    ):
        provider_binding = dict(binding)
        for key in _SUBMISSION_SOURCE_KEYS:
            provider_binding.pop(key, None)
        return provider_binding

    return binding


def _catalog_config_map():
    """Build a validator-slug keyed map of catalog entry config specs."""
    from validibot.validations.validators.base.builtin_configs import BUILTIN_CONFIGS
    from validibot.validations.validators.base.config import discover_configs

    configs = list(discover_configs()) + list(BUILTIN_CONFIGS)
    config_map = {}
    for cfg in configs:
        entries = {}
        for entry in cfg.catalog_entries:
            if entry.entry_type != "signal":
                continue
            entries[(entry.slug, entry.run_stage)] = entry
        config_map[cfg.slug] = entries
    return config_map


def _looks_like_legacy_autogenerated_binding(binding, signal_definition):
    """Return True when a binding still matches the buggy rollout defaults."""
    if binding.source_scope != "submission_payload":
        return False
    if binding.default_value is not None:
        return False
    if binding.is_required is not True:
        return False

    fallback_paths = {
        "",
        signal_definition.contract_key or "",
        signal_definition.native_name or "",
    }
    return binding.source_data_path in fallback_paths


def _normalize_library_signal_bindings(apps, _schema_editor):
    """Repair library-validator bindings and sanitize provider_binding JSON."""
    SignalDefinition = apps.get_model("validations", "SignalDefinition")
    StepSignalBinding = apps.get_model("validations", "StepSignalBinding")
    WorkflowStep = apps.get_model("workflows", "WorkflowStep")

    config_map = _catalog_config_map()

    validator_input_signals = SignalDefinition.objects.filter(
        validator__isnull=False,
        direction="input",
    ).select_related("validator")

    for signal_definition in validator_input_signals:
        validator = signal_definition.validator
        entry = config_map.get(getattr(validator, "slug", ""), {}).get(
            (signal_definition.contract_key, signal_definition.direction),
        )
        fallback_path = (
            signal_definition.native_name
            or signal_definition.contract_key
            or ""
        )
        defaults = _build_step_binding_defaults(
            entry.binding_config if entry else signal_definition.provider_binding,
            fallback_path=fallback_path,
            default_required=entry.is_required if entry else True,
        )

        step_qs = WorkflowStep.objects.filter(validator_id=validator.id)
        for step in step_qs:
            binding, created = StepSignalBinding.objects.get_or_create(
                workflow_step=step,
                signal_definition=signal_definition,
                defaults=defaults,
            )
            if created:
                continue

            if _looks_like_legacy_autogenerated_binding(binding, signal_definition):
                binding.source_scope = defaults["source_scope"]
                binding.source_data_path = defaults["source_data_path"]
                binding.is_required = defaults["is_required"]
                binding.default_value = defaults["default_value"]
                binding.save(
                    update_fields=[
                        "source_scope",
                        "source_data_path",
                        "is_required",
                        "default_value",
                    ],
                )

    for signal_definition in SignalDefinition.objects.filter(validator__isnull=False):
        provider_binding = _normalize_provider_binding(
            signal_definition.provider_binding,
        )
        if provider_binding != (signal_definition.provider_binding or {}):
            signal_definition.provider_binding = provider_binding
            signal_definition.save(update_fields=["provider_binding"])


def _noop(apps, schema_editor):
    """Reverse migration is a no-op."""


class Migration(migrations.Migration):
    dependencies = [
        ("validations", "0034_drop_validatorcatalogentry"),
    ]

    operations = [
        migrations.RunPython(
            _normalize_library_signal_bindings,
            _noop,
            elidable=True,
        ),
    ]
